    cot_reflect_instruction_7 = "Sub-task 7: Verify the derived formula and numeric result by checking consistency with geometric constraints or alternative coordinate approaches (e.g., placing O at origin and A on x-axis) to ensure no unsupported assumptions were made, based on outputs from Sub-task 6."
