    cot_reflect_instruction_7 = "Sub-task 7: Compute the difference r_i - r_o, express it as a simplified fraction m/n where m and n are relatively prime positive integers, ensuring the sign is handled correctly so that m and n are positive, based on Sub-task 6 outputs."
